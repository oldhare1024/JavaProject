public class arr_memory {
    public static void main(String[] args) {
        int[] a = new int[2];
        System.out.println(a);
        System.out.println(a[0]);
        System.out.println(a[1]);
        a[0] = 11;
        a[1] = 22;
        System.out.println(a[0]);
        System.out.println(a[1]);
    }
}
